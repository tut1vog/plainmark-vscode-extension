#!/usr/bin/env node
// Generates docs/spec/_manual-smoke.md — the manual-smoke deliverable: every
// clause whose resolved coverage includes `smoke` (README §5). Shares the clause
// grammar with gen-spec-matrix.mjs. Dependency-free (node:fs / node:path). ESM.

import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs';
import { join, relative, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(fileURLToPath(import.meta.url), '..', '..');
const SPEC_DIR = join(ROOT, 'docs', 'spec');
const OUT_PATH = join(SPEC_DIR, '_manual-smoke.md');

const CLAUSE_RE = /^\s*-\s+\*\*([A-Z][A-Z0-9]*-[A-Z]+-\d+)\*\*((?:\s*`\[[^\]]+\]`)*)\s+—\s+(.+?)\s*$/;
const TAG_RE = /`\[([^\]]+)\]`/g;

function walk(dir, test, out) {
  for (const name of readdirSync(dir)) {
    if (name === 'node_modules' || name === 'dist' || name.startsWith('.')) continue;
    const p = join(dir, name);
    if (statSync(p).isDirectory()) walk(p, test, out);
    else if (test(p)) out.push(p);
  }
}

const clauses = new Map(); // id -> { id, file, statement, tags:Set }
const specFiles = [];
walk(SPEC_DIR, (p) => p.endsWith('.md') && !basename(p).startsWith('_') && basename(p) !== 'README.md', specFiles);

for (const file of specFiles.sort()) {
  const rel = relative(ROOT, file);
  readFileSync(file, 'utf8').split('\n').forEach((text) => {
    const m = CLAUSE_RE.exec(text);
    if (!m) return;
    const [, id, tagRegion, statement] = m;
    const tags = new Set();
    let tm;
    TAG_RE.lastIndex = 0;
    while ((tm = TAG_RE.exec(tagRegion))) tags.add(tm[1]);
    if (!clauses.has(id)) clauses.set(id, { id, file: rel, statement, tags });
  });
}

// Resolve smoke coverage the same way the matrix does (own [smoke] tag or an
// inherited [smoke] clause).
function hasSmoke(id, seen = new Set()) {
  if (seen.has(id)) return false;
  seen.add(id);
  const c = clauses.get(id);
  if (!c) return false;
  if (c.tags.has('smoke')) return true;
  for (const t of c.tags) {
    if (t.startsWith('inherits:') && hasSmoke(t.slice('inherits:'.length), seen)) return true;
  }
  return false;
}

function conformance(c, seen = new Set()) {
  if (c.tags.has('divergent')) return 'divergent';
  if (c.tags.has('unknown')) return 'unknown';
  for (const t of c.tags) {
    if (t.startsWith('inherits:')) {
      const target = clauses.get(t.slice('inherits:'.length));
      if (target && !seen.has(target.id)) return conformance(target, seen.add(c.id));
    }
  }
  if (c.tags.has('accepted')) return 'accepted';
  return 'conforming';
}

const smoke = [...clauses.values()]
  .filter((c) => hasSmoke(c.id))
  .sort((a, b) => a.id.localeCompare(b.id, undefined, { numeric: true }));

const byFile = new Map();
for (const c of smoke) {
  if (!byFile.has(c.file)) byFile.set(c.file, []);
  byFile.get(c.file).push(c);
}

let out = '';
out += '# Manual-Smoke List\n\n';
out += '> GENERATED by `pnpm run spec:smoke` — do not hand-edit.\n';
out += `> ${smoke.length} clauses require manual verification in a real VS Code webview.\n\n`;
out += 'Each clause below carries `[smoke]` coverage (no headless test fully pins it).\n';
out += 'Verify under F5 in the Extension Development Host (Desktop) and vscode.dev (Web).\n';
out += 'A `conformance` other than `conforming` means the clause also awaits a ruling\n';
out += '(`unknown` → confirm by smoke; cross-referenced in `_decision-points.md`).\n\n';

for (const [file, cs] of [...byFile.entries()].sort()) {
  out += `## \`${file}\`\n\n`;
  out += '| Clause | Conformance | Behavior to verify |\n|---|---|---|\n';
  for (const c of cs) {
    const stmt = c.statement.replace(/\|/g, '\\|');
    out += `| ${c.id} | ${conformance(c)} | ${stmt} |\n`;
  }
  out += '\n';
}

writeFileSync(OUT_PATH, out);
console.log(`wrote ${relative(ROOT, OUT_PATH)}`);
console.log(`manual-smoke: ${smoke.length} clauses across ${byFile.size} files`);
